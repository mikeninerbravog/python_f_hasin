# Define the main function
def main():
    # Create a string 'country_name' containing the name of a country in lowercase
    country_name = 'bangladesh'

    # Use the 'capitalize()' method to capitalize the first letter of the string
    capitalized_country_name = country_name.capitalize()

    # Print the capitalized string
    print(capitalized_country_name)

# Check if this script is the main entry point
if __name__ == '__main__':
    # Call the 'main' function if this script is being run directly
    main()

# The expected output: The string 'bangladesh' with the first letter capitalized
# 'Bangladesh'
