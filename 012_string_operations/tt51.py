# Define the main function
def main():
    # Define a string 'book_name' with a lowercase title
    book_name = 'the house of silk'

    # Use the 'title()' method to capitalize the first letter of each word in the string
    capitalized_title = book_name.title()

    # Print the capitalized title
    print(capitalized_title)

# Check if this script is the main entry point
if __name__ == '__main__':
    # Call the 'main' function if this script is being run directly
    main()

# The expected output: The string 'the house of silk' with each word's first letter capitalized
# The House Of Silk
