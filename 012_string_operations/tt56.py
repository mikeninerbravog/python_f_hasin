# Define the main function
def main():
    # Define a string 'book_name'
    book_name = 'moriarty'

    # Print the uppercase version of the 'book_name' string using the 'upper()' method
    print(book_name.upper())  # Convert all characters to uppercase and print

    # Define another string 'another_book_name'
    another_book_name = 'DRACULA'

    # Print the lowercase version of the 'another_book_name' string using the 'lower()' method
    print(another_book_name.lower())  # Convert all characters to lowercase and print

# Check if this script is the main entry point
if __name__ == '__main__':
    # Call the 'main' function if this script is being run directly
    main()

# The expected output:
# MORIARTY
# dracula
