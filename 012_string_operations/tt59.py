# Define the main function
def main():
    # Define a string 'book_name' in uppercase
    book_name = 'HEARTS IN ATLANTIS'

    # Print the 'book_name' with swapped case using the 'swapcase()' method
    print(book_name.swapcase())  # Swap the case of each character and print

# Check if this script is the main entry point
if __name__ == '__main__':
    # Call the 'main' function if this script is being run directly
    main()

# The expected output: The string 'HEARTS IN ATLANTIS' with the case of each character swapped
# Resulting in 'hearts in atlantis'
