# Define the main function
def main():
    # Initialize a variable 'number' with the value 1
    number = 1

    # Use a 'while' loop to execute the following block of code as long as 'number' is less than 11
    while number < 11:
        # Print the current value of 'number'
        print(number)

        # Increment the value of 'number' by 1
        number += 1


# Check if this script is the main entry point
if __name__ == '__main__':
    # Call the 'main' function if this script is being run directly
    main()

