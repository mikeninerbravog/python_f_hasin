# Define a function named 'main'
def main():
    # Assign the value -5.8 to the variable 'num_1'
    num_1 = -5.8

    # Calculate the absolute value of 'num_1' using the 'abs' function
    absolute_value = abs(num_1)

    # Use an f-string to format and print the result
    print(f'the absolute value of {num_1} is: {absolute_value}')


# Check if this script is being run as the main program
if __name__ == '__main__':
    # Call the 'main' function if this script is the main program
    main()

