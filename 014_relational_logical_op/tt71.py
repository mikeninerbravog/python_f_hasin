# Define the main function
def main():
    # Using the NOT operator to negate the boolean values
    print('not True =', not True)   # Logical NOT of True is False
    print('not False =', not False)  # Logical NOT of False is True

# Check if this script is the main entry point
if __name__ == '__main__':
    # Call the 'main' function if this script is being run directly
    main()

# Example output:
# not True = False
# not False = True
